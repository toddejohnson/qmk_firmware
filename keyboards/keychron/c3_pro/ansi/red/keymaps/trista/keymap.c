bool led_matrix_indicators_user(void) {
    bool caps = host_keyboard_led_state().caps_lock;
    if (caps) {
        // If Capslock turn off caps lock and shift leds
        led_matrix_set_value(50, 0);
        led_matrix_set_value(63, 0);
        led_matrix_set_value(74, 0);
    } else {
        led_matrix_set_value(50, 255);
        led_matrix_set_value(63, 255);
        led_matrix_set_value(74, 255);
    }

    if (custom_name_timer_buffer && timer_elapsed32(custom_name_timer_buffer) > 1500) {
        // Timer expired and is set
        if (custom_name_count > sizeof(custom_name_keys[caps])) {
            // We're out of char, reset
            custom_name_count = 0;
            custom_name_timer_buffer   = 0;
            led_matrix_set_value_all(255);
        } else {
            // keep going
            led_matrix_set_value_all(0);
            led_matrix_set_value(custom_name_keys[caps][custom_name_count], 255);
            custom_name_count++;
            custom_name_timer_buffer = timer_read32();
        }
    } else if (custom_name_timer_buffer && custom_name_count <= sizeof(custom_name_keys[caps])-1) {
        // timer set but not expired. Force State again
        led_matrix_set_value_all(0);
        led_matrix_set_value(custom_name_keys[caps][custom_name_count], 255);
    }

    // led_matrix_set_value(41, 0);
    // led_matrix_set_value(59, 0);
    // led_matrix_set_value(40, 0);
    // led_matrix_set_value(67, 0);
    return true;
}


void keyboard_post_init_user(void) {
  // Call the post init code.
//   led_matrix_enable_noeeprom();
    custom_name_timer_buffer = timer_read32();
    custom_name_count = 0;
}
